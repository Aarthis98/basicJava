package May_18;

public class Comparison {
    public static void main(String[] args){

            /*
            May_18.Comparison Operators
            ==
            !=
            <
            >
            >=
            <=

             */
            int x = 10;
            int y = 20;
            int a = 15, b = 25;


            System.out.println(x == y);
            System.out.println(x < y);
            System.out.println(x > y);
            System.out.println(x >= y);
            System.out.println(x <= y);
            System.out.println(x != y);
            System.out.println(a > b);
            System.out.println(a < x);
            System.out.println(b == y);
            System.out.println(a >= y);
            System.out.println(b <= y);
            System.out.println(b != y);
    }
}
