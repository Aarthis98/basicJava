package June_01;



public class CalcMain {



     public void sum(int a ,int b){
         int result = a + b;
         System.out.println(a + " + " + b + " = " +result);

    }
    public void sub(int a , int b){
        int result = a - b;
        System.out.println(a + " - " + b + " = " +result);

    }
    public void multiply(int a , int b){
        int result = a * b;
        System.out.println(a + " * " + b + " = " +result);

    }
    public void divide(int a , int b){
        int result = a / b;
        System.out.println(a + " / " + b + " = " +result);

     }
    public void modulos(int a , int b){
        int result = a % b;
        System.out.println(a + " % " + b + " = " +result);

    }



}